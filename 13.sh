#!/bin/bash

# Проверка наличия трех аргументов
if [ "$#" -ne 3 ]; then
  echo "Ошибка: Скрипт требует три аргумента."
  echo "Использование: $0 A B C"
  exit 1
fi

# Присваивание аргументов переменным
A=$1
B=$2
C=$3

# Проверка, что A не равно нулю
if [ "$A" -eq 0 ]; then
  echo "Ошибка: Коэффициент A не может быть равен нулю."
  exit 1
fi

# Вычисление дискриминанта
D=$(echo "$B * $B - 4 * $A * $C" | bc -l)

# Проверка значения дискриминанта и нахождение корней
if (( $(echo "$D < 0" | bc -l) )); then
  # Если дискриминант меньше нуля, корней нет
  exit 0
elif (( $(echo "$D == 0" | bc -l) )); then
  # Если дискриминант равен нулю, один корень
  root=$(echo "scale=10; -1 * $B / (2 * $A)" | bc -l)
  echo "Корень: $root"
else
  # Если дискриминант больше нуля, два корня
  sqrt_D=$(echo "scale=10; sqrt($D)" | bc -l)
  root1=$(echo "scale=10; (-1 * $B + $sqrt_D) / (2 * $A)" | bc -l)
  root2=$(echo "scale=10; (-1 * $B - $sqrt_D) / (2 * $A)" | bc -l)
  echo "Корни: $root1 и $root2"
fi


#!/bin/bash

# Проверка наличия аргумента
if [ -z "$1" ]; then
  echo "Ошибка: директория не указана."
  echo "Использование: $0 <directory>"
  exit 1
fi

# Переменная для директории
DIRECTORY=$1

# Выполнение команды find и подсчет количества строк
FILE_COUNT=$(find "$DIRECTORY" -type f -mtime -3 | wc -l)

# Вывод результата
echo "Количество файлов, измененных за последние 3 дня: $FILE_COUNT"

